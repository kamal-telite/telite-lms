def _course_progress(
    sequence: list[str],
    completed_count: int,
    active_course_id: str | None = None,
    active_progress: int = 0,
    active_lesson: str | None = None,
) -> list[dict[str, Any]]:
    items: list[dict[str, Any]] = []
    for index, course_id in enumerate(sequence):
        if index < completed_count:
            items.append(
                {
                    "course_id": course_id,
                    "progress": 100,
                    "status": "completed",
                    "current_lesson": None,
                }
            )
            continue

        if course_id == active_course_id:
            items.append(
                {
                    "course_id": course_id,
                    "progress": active_progress,
                    "status": "in_progress",
                    "current_lesson": active_lesson,
                }
            )
            continue

        items.append(
            {
                "course_id": course_id,
                "progress": 0,
                "status": "locked" if index >= 3 and completed_count < 3 else "not_started",
                "current_lesson": None,
            }
        )

    return items


